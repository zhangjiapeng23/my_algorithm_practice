class Solution:

    def isMatch(self, s: str, p: str) -> bool:
        n = len(s)
        m = len(p)
        dp = [[False] * (m + 1) for _ in range(n+1)]

        # åˆå§‹åŒ–è¾¹ç•Œæ¡ä»¶
        # ï¼ˆ1ï¼‰å½“éƒ½ä¸ºç©ºæ˜¯ï¼Œæ˜¯True
        # ï¼ˆ2ï¼‰å½“s ä¸ºç©ºæ—¶ï¼Œpåªæœ‰ä¸º * æ‰èƒ½åŒ¹é…ï¼Œ ä¸”pçš„*å¿…é¡»è¿ç»­
        # ï¼ˆ3ï¼‰å½“p ä¸ºç©ºæ—¶ï¼Œä¸€å®šä¸èƒ½åŒ¹é…
        dp[0][0] = True
        for i in range(1, m+1):
            if p[i-1] == '*':
                dp[0][i] = True
            else:
                break

        for i in range(1, n+1):
            for j in range(1, m+1):
                if p[j-1] == '*':
                    dp[i][j] = dp[i][j-1] or dp[i-1][j]
                elif s[i-1] == p[j-1] or p[j-1] == '?':
                    dp[i][j] = dp[i-1][j-1]

        return dp[n][m]


